public class TiposVariaveis {

    public static void main(String[] args) throws Exception {
        /*
         * O método "final" serve para variáveis que terão
         * o mesmo valor sempre, sem trocas. Sua sintaxe fica:
         */

        final double VALOR_DE_PI = 3.14;

        /*
         * Caso tente mudar seu valor:
         * valorDePi = 5;
         * 
         * O código apontará erro, pois variáveis declaradas com
         * final nao podem ter seu valor mudado.
         * 
         * A sintaxe de uma variavel que possui final na sua declaração
         * também deve ser feita em caixa alta, como padrão usado no JAVA
         * para esse tipo de variável. exemplo:
         * 
         * final double VALOR_DE_PI = 3.14;
         */

    }
}
